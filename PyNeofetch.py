import subprocess

def get_cpu_info():
    try:
        return subprocess.check_output("lscpu | grep 'Model name'", shell=True).decode().split(':')[1].strip()
    except:
        return "Unknown"


def get_system_info():
    info = {
        "OS": f"{platform.system()} {platform.release()} ({platform.version()})",
        "Kernel": platform.uname().release,
        "Uptime": f"{psutil.boot_time():.0f} seconds since boot",
        "CPU": get_cpu_info(),
        "CPU Cores": psutil.cpu_count(logical=False),
        "Logical Cores": psutil.cpu_count(logical=True),
        "RAM": f"{round(psutil.virtual_memory().total / (1024**3), 2)} GB",
        "Disk Space": f"{round(shutil.disk_usage('/').total / (1024**3), 2)} GB",
        "Python Version": platform.python_version()
    }
    return info

def display_info():
    banner()
    info = get_system_info()
    max_key_length = max(len(key) for key in info)
    for key, value in info.items():
        print(f"\033[94m{key.ljust(max_key_length)}\033[0m : \033[92m{value}\033[0m")

if __name__ == "__main__":
    display_info()
